
public class SoalUAS4 {
	public static void main(String[] args) {
		  int i = 0;
	       for (int n = 5; n >= 1; n--){
	           for (int j = n - 0; j >= 0; j--){
	               System.out.print(" ");
	           }
	           for (int l = 0; l <= i; l++){
	               System.out.print(l);
	           }
	           System.out.println();
	           i += 1;
	       }
	   
	       }
	   
	
}
